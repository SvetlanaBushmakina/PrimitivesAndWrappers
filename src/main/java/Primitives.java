public class Primitives {
    public static void main(String[] args) {

        boolean a = true;           // a = true
        byte b = 3;                 // b = 3
        short c = 8;                // c = 8
        int d = 10;                 // d = 10
        long e = 20000000000000L;   // e = 20000000000000
        double f = 5.8;             // f = 5.8
        float g = 4.9f;             // g = 4.9
        char ch = 102;              // ch = f

    }
}
